-- -----------------------------------------------------
-- Table `TripBook`.`user`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `TripBook`.`user` (
  `id_user` INT NOT NULL AUTO_INCREMENT,
  `firstname` VARCHAR(255) NOT NULL,
  `lastname` VARCHAR(255) NOT NULL,
  `email` VARCHAR(255) NOT NULL,
  `phone_number` INT NOT NULL,
